a = int(input("Enter a number: "))     #type casting from string to int (Explicit)
b = int(input("Enter a number: "))
print("Sum of",a,"and",b,"is",a+b)
print("Difference of",a,"and",b,"is",a-b)
print("Product of",a,"and",b,"is",a*b)
print("Division of",a,"and",b,"is",a/b)
print("Modulo of",a,"and",b,"is",a%b)
print("Power of",a,"and",b,"is",a**b)

print(a>b)              #greater than
print(a<b)              #less than
print(a==b)             #equal to
print(a!=b)             #not equal to
print(a>=b)             #greater than or equal to
print(a<=b)             #less than or equal to

print((5>10) and (10==10))          #logical AND
print((5>10) or (10==10))           #logical OR
print(not (5>10))            #logical NOT

